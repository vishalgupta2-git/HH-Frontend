// Helper function to get translation
export const getNavratriTranslation = (key: string, language: string, deityName?: string): string => {
  const keys = key.split('.');
  let translation: any = navratriTranslations;
  
  for (const k of keys) {
    translation = translation?.[k];
  }
  
  if (typeof translation === 'object' && translation[language]) {
    let result = translation[language];
    if (deityName && result.includes('{deityName}')) {
      result = result.replace('{deityName}', deityName);
    }
    return result;
  }
  
  // Fallback to English if translation not found
  if (typeof translation === 'object' && translation.english) {
    let result = translation.english;
    if (deityName && result.includes('{deityName}')) {
      result = result.replace('{deityName}', deityName);
    }
    return result;
  }
  
  return key; // Return key if no translation found
};
